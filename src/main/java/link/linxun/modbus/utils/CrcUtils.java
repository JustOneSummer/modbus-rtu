    /**
     * crc验证
     * 判断CRC是否相等(有高低位) 互换一下才是正确的结果
     *
     * @param data    crc数据
     * @param dataLen 数据长度
     * @return 结果
     */
    public static short check(byte[] data, int dataLen) {
        short uchCRCHi = 0xff;
        short uchCRCLo = 0xff;
        int uindex;

        for (int i = 0; i < dataLen; i++) {
            uindex = (uchCRCLo ^ data[i]) & 0x00ff;
            uchCRCLo = (short) (uchCRCHi ^ AUCH_CRC_HI[uindex]);
            uchCRCHi = AUCH_CRC_LO[uindex];
        }
        return (short) (((uchCRCHi & 0x00ff) << 8) | (uchCRCLo & 0x00ff) & 0xffff);
    }


